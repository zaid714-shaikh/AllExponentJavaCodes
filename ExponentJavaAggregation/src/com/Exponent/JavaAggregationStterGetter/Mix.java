package com.Exponent.JavaAggregationStterGetter;

public class Mix {

	int eid;
	String ename;
	
	public Mix(int eid, String ename) {
		super();
		this.eid = eid;
		this.ename = ename;
	}
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	
	public Mix()
	{
		
	}
}
