package com.Exponent.SetterGetterProject;

public class Pen {

	private int penriffils;
	private String color;
	private String pencompany;

	public void setPenriffils(int pr) {
		penriffils = pr;
	}

	public int getpenriffils() {
		return penriffils;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getPencompany() {
		return pencompany;
	}

	public void setPencompany(String pencompany) {
		this.pencompany = pencompany;
	}

}
