package com.pojo;

public class student {

	int sid;
	String sname;
	long spassword;

	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

	public long getSpassword() {
		return spassword;
	}

	public void setSpassword(long spassword) {
		this.spassword = spassword;
	}
}
